/*
 * 1). Snowflake Setup - Creating Database, Schemas and Warehouse
 */

USE ROLE SYSADMIN;

CREATE DATABASE RETAIL_ANALYTICS;

CREATE SCHEMA RAW;
CREATE SCHEMA STAGING;
CREATE SCHEMA MART;

CREATE WAREHOUSE RETAIL_WH
WITH 
    WAREHOUSE_SIZE = 'XSMALL'
    AUTO_SUSPEND = 60
    AUTO_RESUME = TRUE;


/*
 * 2). Creating File Format and Stage
 */

USE ROLE SYSADMIN;
USE DATABASE RETAIL_ANALYTICS;
USE SCHEMA RAW;

CREATE OR REPLACE FILE FORMAT RAW.CSV_FORMAT
TYPE = CSV
FIELD_OPTIONALLY_ENCLOSED_BY='\"'
SKIP_HEADER = 1;

CREATE OR REPLACE STAGE RAW.RETAIL_STAGE
FILE_FORMAT = RAW.CSV_FORMAT;


/*
 * 3). Creating Raw Tables
 */

USE ROLE SYSADMIN;
USE DATABASE RETAIL_ANALYTICS;
USE SCHEMA RAW;

CREATE OR REPLACE TABLE RAW.RAW_SALES (
    ORDER_ID STRING,
    ORDER_DATE DATE,
    CUSTOMER_ID STRING,
    PRODUCT_ID STRING,
    REGION_ID STRING,
    QUANTITY NUMBER,
    SALES_AMOUNT NUMBER(10,2),
    PROFIT NUMBER(10,2),
    DISCOUNT NUMBER(5,2)
);

CREATE OR REPLACE TABLE RAW.DIM_CUSTOMER (
    CUSTOMER_ID STRING,
    CUSTOMER_NAME STRING,
    SEGMENT STRING,
    CITY STRING,
    STATE STRING,
    COUNTRY STRING
);

CREATE OR REPLACE TABLE RAW.DIM_PRODUCT (
    PRODUCT_ID STRING,
    PRODUCT_NAME STRING,
    CATEGORY STRING,
    SUB_CATEGORY STRING,
    BRAND STRING
);

CREATE OR REPLACE TABLE RAW.DIM_REGION (
    REGION_ID STRING,
    REGION_NAME STRING,
    MANAGER STRING
);

CREATE OR REPLACE TABLE RAW.DIM_DATE (
    DATE_KEY STRING,
    FULL_DATE DATE,
    MONTH STRING,
    QUARTER NUMBER,
    YEAR NUMBER
);

